CREATE DATABASE ChefExpress;

GO

USE ChefExpress;

CREATE TABLE Employee (
	EmployeeNum INT PRIMARY KEY,
	Name VARCHAR(20),
	Birthday DATE,
	Salary INT
);

CREATE TABLE Manager (
	EmployeeNum INT PRIMARY KEY,
	Complaints INT,
	FOREIGN KEY (EmployeeNum)
		REFERENCES Employee (EmployeeNum)
);

CREATE TABLE Chef (
	EmployeeNum INT PRIMARY KEY,
	Country VARCHAR(20),
	FOREIGN KEY (EmployeeNum)
		REFERENCES Employee (EmployeeNum)
);

CREATE TABLE Cook (
	EmployeeNum INT PRIMARY KEY,
	Seniority INT,
	FOREIGN KEY (EmployeeNum)
		REFERENCES Employee (EmployeeNum)
);

CREATE TABLE Cashier (
	EmployeeNum INT PRIMARY KEY,
	Gender VARCHAR(1),
	FOREIGN KEY (EmployeeNum)
		REFERENCES Employee (EmployeeNum)
);

CREATE TABLE Shift (
	ShiftNum INT PRIMARY KEY,
	ShiftDesc VARCHAR(100),
	NumOfCashiers INT NOT NULL,
	NumOfCooks INT NOT NULL
);

CREATE TABLE ShiftInDate (
	ShiftNum INT,
	ShiftDate DATE,
	StartingTime TIME,
	EndingTime TIME,
	ChefNum INT NOT NULL,
	ManagerNum INT NOT NULL,
	PRIMARY KEY (ShiftNum, ShiftDate),
	FOREIGN KEY (ShiftNum)
		REFERENCES Shift (ShiftNum)
			ON DELETE CASCADE,
	FOREIGN KEY (ChefNum) REFERENCES Chef (EmployeeNum),
	FOREIGN KEY (ManagerNum) REFERENCES Manager (EmployeeNum)
);

CREATE TABLE CooksInShifts (
	ShiftNum INT,
	ShiftDate DATE,
	CookNum INT,
	PRIMARY KEY (ShiftNum, ShiftDate, CookNum),
	FOREIGN KEY (ShiftNum, ShiftDate) REFERENCES ShiftInDate (ShiftNum, ShiftDate),
	FOREIGN KEY (CookNum) REFERENCES Cook (EmployeeNum)
);

CREATE TABLE CashiersInShifts (
	ShiftNum INT,
	ShiftDate DATE,
	CashierNum INT,
	PRIMARY KEY (ShiftNum, ShiftDate, CashierNum),
	FOREIGN KEY (ShiftNum, ShiftDate) REFERENCES ShiftInDate (ShiftNum, ShiftDate),
	FOREIGN KEY (CashierNum) REFERENCES Cashier (EmployeeNum)
);

CREATE TABLE Customer (
	CustomerNum INT PRIMARY KEY,
	Name VARCHAR(20),
	PhoneNum VARCHAR(11)
);

CREATE TABLE Orders (
	OrderNum INT PRIMARY KEY,
	Payment VARCHAR(20),
	CustomerNum INT NOT NULL,
	ShiftNum INT NOT NULL,
	ShiftDate DATE NOT NULL,
	CashierNum INT NOT NULL,
	FOREIGN KEY (ShiftNum, ShiftDate, CashierNum) REFERENCES CashiersInShifts (ShiftNum, ShiftDate, CashierNum),
	FOREIGN KEY (CustomerNum) REFERENCES Customer (CustomerNum)
);

CREATE TABLE Course (
	CourseNum INT PRIMARY KEY,
	Name VARCHAR(20),
	CourseDesc VARCHAR(100),
	Price INT
);

CREATE TABLE CoursesInOrder (
	OrderNum INT,
	CourseNum INT,
	Quantity INT,
	PRIMARY KEY (OrderNum, CourseNum),
	FOREIGN KEY (OrderNum) REFERENCES Orders (OrderNum),
	FOREIGN KEY (CourseNum) REFERENCES Course (CourseNum)
);


